public class SarumanAndLannister {
    public static void main(String[] args) {

        final int warriorLannister = 1234;
        System.out.println("У Ланнистера в войске " + warriorLannister + " пехотинцев");

        final int orcsSaruman = 1543;
        System.out.println("Войско Сарумана составляет " + orcsSaruman + " орков");

        final int unionSarumanAndLannister = warriorLannister + orcsSaruman;
        System.out.println("В союзе Сарумана и Ланнистера " + unionSarumanAndLannister + " пехотинцев");

    }
}
